export const DICTIONARIES: Record<Lang, Dict> = { en: EN, fr: FR };

export const SUPPORTED_LANGUAGES: Lang[] = ['en', 'fr'];

export const getLanguage = (): Lang => {
  if (typeof window === 'undefined') return 'en';
  const stored = localStorage.getItem('app_language') as Lang | null;
  if (stored && SUPPORTED_LANGUAGES.includes(stored)) return stored;
  return 'en';
};

export const setLanguage = (lang: Lang): void => {
  localStorage.setItem('app_language', lang);
};

export const t = (key: string, ...args: (string | number)[]): string => {
  const lang = getLanguage();
  const val = DICTIONARIES[lang]?.[key];
  if (val === undefined) {
    const enVal = DICTIONARIES['en'][key];
    if (enVal === undefined) return key;
    if (args.length === 0) return enVal;
    return enVal.replace(/\{(\d+)\}/g, (_, idx) => String(args[Number(idx)] ?? ''));
  }
  if (args.length === 0) return val;
  return val.replace(/\{(\d+)\}/g, (_, idx) => String(args[Number(idx)] ?? ''));
};
